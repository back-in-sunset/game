func (u *customCQLUserModel) Update(ctx context.Context, data *User) error {
	olduser, err := u.FindOne(ctx, data.UserID)
	if err != nil {
		return err
	}

	batch := u.Session.NewBatch(gocql.LoggedBatch)

	// 删除旧 mobile
	if olduser.Mobile != "" && olduser.Mobile != data.Mobile {
		batch.Query(`DELETE FROM %s WHERE mobile=?`, UsersByMobile{}.TableName(), olduser.Mobile)
	}

	// 插入新 mobile
	if data.Mobile != "" && olduser.Mobile != data.Mobile {
		batch.Query(`
            INSERT INTO %s (mobile, user_id, email, name)
            VALUES (?, ?, ?, ?)
        `, UsersByMobile{}.TableName(), data.Mobile, data.UserID, data.Email, data.Name)
	}

	// 删除旧 email
	if olduser.Email != "" && olduser.Email != data.Email {
		batch.Query(`DELETE FROM %s WHERE email=?`, UsersByEmail{}.TableName(), olduser.Email)
	}

	// 插入新 email
	if data.Email != "" && olduser.Email != data.Email {
		batch.Query(`
            INSERT INTO %s (email, user_id, mobile, name)
            VALUES (?, ?, ?, ?)
        `, UsersByEmail{}.TableName(), data.Email, data.UserID, data.Mobile, data.Name)
	}

	// 更新主表
	batch.Query(`
        UPDATE %s
        SET mobile=?, email=?, name=?, update_at=toTimestamp(now())
        WHERE user_id=?
    `, u.TableName(), data.Mobile, data.Email, data.Name, data.UserID)

	err = u.Session.ExecuteBatch(batch)
	if err != nil {
		return err
	}

	return u.cache.DelCtx(ctx, cacheUserUserIDPrefix, cacheUserMobilePrefix)
}

func (u *customCQLUserModel) Delete(ctx context.Context, id int64) error {
	batch := u.Session.NewBatch(gocql.LoggedBatch)
	batch.Query(`DELETE FROM %s WHERE user_id=?`, u.TableName(), id)
	batch.Query(`DELETE FROM %s WHERE user_id=?`, UsersByMobile{}.TableName(), id)
	batch.Query(`DELETE FROM %s WHERE user_id=?`, UsersByEmail{}.TableName(), id)

	// 执行批量删除
	err := u.Session.ExecuteBatch(batch)
	if err != nil {
		return err
	}

	return u.cache.DelCtx(ctx, cacheUserUserIDPrefix, cacheUserMobilePrefix)
}

